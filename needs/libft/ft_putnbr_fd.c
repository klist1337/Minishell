#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	long	n2;

	n2 = n;
	if (n < 0)
	{
		n2 *= -1;
		write(fd, "-", 1);
	}
	if (n2 / 10 == 0)
	{
		c = n2 + '0';
		write(fd, &c, 1);
	}
	else
	{
		ft_putnbr_fd(n2 / 10, fd);
		n2 = n2 % 10;
		c = n2 + '0';
		write(fd, &c, 1);
	}
}
